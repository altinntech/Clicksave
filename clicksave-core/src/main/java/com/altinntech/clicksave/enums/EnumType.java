package com.altinntech.clicksave.enums;

/**
 * The {@code EnumType} enum defines different types of enums.
 * It includes STRING, ORDINAL, and BY_ID.
 *
 * @author Fyodor Plotnikov
 */
public enum EnumType {

    /**
     * Indicates a string-based enum type.
     */
    STRING,

    /**
     * Indicates an ordinal-based enum type.
     */
    ORDINAL,

    /**
     * Indicates an enum type based on ID.
     */
    BY_ID,
}

